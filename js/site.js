function renderSiteLinks(containerId, items) {
  const container = document.getElementById(containerId);
  if (!container) return;

  container.innerHTML = items.map(item => `
    <a href="${item.href}"
       class="site-link-item"
       target="_blank"
       rel="noopener noreferrer">

      <div class="site-link-item__icon-box">
        <img src="${item.img}" alt="${item.title}" class="site-link-item__icon">
      </div>

      <span class="site-link-item__title">
        ${item.title}
      </span>
    </a>
  `).join("");
}

